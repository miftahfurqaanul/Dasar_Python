set1 = {1, 2, 3, 4, 5}
set2 = {3, 4, 5, 6, 7}

# Print elements in both sets
print("Set 1:", set1)
print("Set 2:", set2)

# Calculate and print the intersection
intersection = set1.intersection(set2)
print("Intersection:", intersection)

# Add a new element to one of the sets
set1.add(8)
print("Updated Set 1:", set1)